package Colisiones;

import Disparo.Disparo;
import Enemigo.Enemigo;
import Jugador.Jugador;
import Obstaculo.ObstaculoDestructible;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class ColDispEnemigo extends ColisionadorDisparo
{
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public ColDispEnemigo( )
	{
	}

	///////////////////////////////////////////////////////////////////////////////////////////////

	public ColisionadorDisparo clone()
	{
		return new ColDispEnemigo( );
	}
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public void afectar(Jugador jugador)
	{
		jugador.recibirDMG( disparo.getDmg() );
		disparo.remove();
	}

	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public void afectar(ObstaculoDestructible obstaculo)
	{
		obstaculo.recibirDMG( disparo.getDmg() );
		disparo.remove();
	}

	///////////////////////////////////////////////////////////////////////////////////////////////
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////