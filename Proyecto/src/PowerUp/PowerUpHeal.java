package PowerUp;

import java.awt.Color;

import javax.swing.JPanel;

import Colisiones.ColisionadorPowerup;
import Escudo.EscudoHealer;
import Jugador.Jugador;
import Mapa.Mapa;
import Utils.Posicion;
import Utils.Size;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

public class PowerUpHeal extends PowerUp
{
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public PowerUpHeal( Mapa map )
	{
		this.map			= map;
		this.pos			= new Posicion( 0, 0 );
		this.panel			= new JPanel( );
		this.tamano			= new Size( 30, 30 );
		this.vida			= 1000;
		this.colisionador	= new ColisionadorPowerup( this );
		
		this.actualizarPanel(true, Color.green);
	}
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public void afectar(Jugador player)
	{
		System.out.println("Has agarrado el PowerUpHeal, tu salud se recupera lentamente!");
		player.addEscudo( new EscudoHealer(player) );
	}

	///////////////////////////////////////////////////////////////////////////////////////////////
}

